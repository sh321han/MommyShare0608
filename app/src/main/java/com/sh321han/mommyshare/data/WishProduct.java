public class WishProduct {
    String wish_name;
    int wish_price;
    int wish_deposit;
    int wish_minPeriod;
    int wish_maxPeriod;

    public String getWish_name() {
        return wish_name;
    }

    public void setWish_name(String wish_name) {
        this.wish_name = wish_name;
    }

    public int getWish_price() {
        return wish_price;
    }

    public void setWish_price(int wish_price) {
        this.wish_price = wish_price;
    }

    public int getWish_deposit() {
        return wish_deposit;
    }

    public void setWish_deposit(int wish_deposit) {
        this.wish_deposit = wish_deposit;
    }

    public int getWish_minPeriod() {
        return wish_minPeriod;
    }

    public void setWish_minPeriod(int wish_minPeriod) {
        this.wish_minPeriod = wish_minPeriod;
    }

    public int getWish_maxPeriod() {
        return wish_maxPeriod;
    }

    public void setWish_maxPeriod(int wish_maxPeriod) {
        this.wish_maxPeriod = wish_maxPeriod;
    }
}


