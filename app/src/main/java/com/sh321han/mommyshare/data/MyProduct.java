public class MyProduct {
    String my_name;
    int my_price;
    int my_deposit;
    int my_minPeriod;
    int my_maxPeriod;

    public String getMy_name() {
        return my_name;
    }

    public void setMy_name(String my_name) {
        this.my_name = my_name;
    }

    public int getMy_price() {
        return my_price;
    }

    public void setMy_price(int my_price) {
        this.my_price = my_price;
    }

    public int getMy_deposit() {
        return my_deposit;
    }

    public void setMy_deposit(int my_deposit) {
        this.my_deposit = my_deposit;
    }

    public int getMy_minPeriod() {
        return my_minPeriod;
    }

    public void setMy_minPeriod(int my_minPeriod) {
        this.my_minPeriod = my_minPeriod;
    }

    public int getMy_maxPeriod() {
        return my_maxPeriod;
    }

    public void setMy_maxPeriod(int my_maxPeriod) {
        this.my_maxPeriod = my_maxPeriod;
    }
}

